#include "./libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!s1 || !s2)
		return (0);
	if (n == 0)
		return (1);
	while (*s1 != '\0' && *s2 != '\0' && n > 0)
	{
		if (*s1 == *s2)
		{
			s1++;
			s2++;
		}
		else
			return (0);
		n--;
	}
	s1--;
	s2--;
	if (*s2 == *s1)
		return (1);
	return (0);
}
